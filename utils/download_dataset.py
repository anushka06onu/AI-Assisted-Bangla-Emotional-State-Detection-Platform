    def generate_class_data(nouns, modifiers, cores, endings, label, count=500):
        unique_sentences = set()
        max_attempts = 10000
        attempts = 0
        
        while len(unique_sentences) < count and attempts < max_attempts:
            attempts += 1
            n = random.choice(nouns)
            m = random.choice(modifiers)
            c = random.choice(cores)
            e = random.choice(endings)
            
            # Combine grammatically
            sentence = f"{n} {m} {c} {e}"
            unique_sentences.add(sentence)
            
        print(f"Generated {len(unique_sentences)} unique sentences for class '{label}' (after {attempts} attempts).")
        return pd.DataFrame({'text': list(unique_sentences), 'label': label})
    
